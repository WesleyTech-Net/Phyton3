#2 while
tabuada  = 1
while tabuada <= 10:
    print(f'\nTABUADA DO {tabuada}')
    i = 1
    while i <= 10:
        print(f'{tabuada} X {i} = {tabuada * i}')
        i += 1
    tabuada += 1
