#a) inteiros de 3 até 12, com 12 incluso
#inteiro de 0 até 9, excluindo 9, com passo de 2

#a) com WHILE
i = 3

while (i < 13):
    print(i)
    i += 1

#a) com FOR
for i in range(3, 13):
    print(i)

#b)com FOR
for i in range(0, 9, 2):
    print(i)

#b)com WHILE
i = 0

while (i < 9):
    print(i)
    i += 2